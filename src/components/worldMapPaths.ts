// High-detail world map continent outlines for equirectangular projection (800x400)
// Stylized but geographically accurate paths

function toPath(coords: [number, number][]): string {
  return coords
    .map(([lng, lat], i) => {
      const x = ((lng + 180) / 360) * 800;
      const y = ((90 - lat) / 180) * 400;
      return `${i === 0 ? 'M' : 'L'}${x.toFixed(1)},${y.toFixed(1)}`;
    })
    .join(' ') + 'Z';
}

